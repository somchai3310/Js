age = 15;

// if statement
if (age == 15) {
  console.log("อายุ 15 ปี");
}

// if else
if (age > 15) {
  console.log("อายุเกิน 15 ปี");
} else {
  console.log("อายุต่ำ 15 ปี");
}

// tenary operator
console.log(age ? "อายุเกิน 15 ปี" : "อายุต่ำ 15 ปี");
